        <h1 id="first-type">1.APA ITU REKENING BERSAMA?</h1>
        <div>
            <p>
                Rekening Bersama adalah rekening penampung dana atas nama PT Insta Shop Indonesia yang menjamin keamanan dana hingga transaksi selesai.
            </p>
            <p>Dengan menggunakan fitur rekening bersama ini, maka dana pembeli akan disimpan di rekening Instashop dan <strong>baru dicairkan ke rekening penjual setelah barang diterima pembeli.</strong></p>
            <p>Fitur Rekening Bersama saat ini hanya dapat dinikmati oleh pembeli yang berbelanja via <strong>WEB</strong> dengan catatan fitur ini sudah diaktifkan penjual. Untuk pesanan yang masuk melalui fitur <strong>Buat Pesanan</strong> atau Penagihan Instan hanya dapat menikmati fitur <strong>Transfer Langsung</strong>.</p>
            <p><strong>Bagaimana Proses Transaksi di Rekening Bersama?</strong></p>
            <p>1. Pembeli mengajukan pesanan di Web.</p>
            <p>2. Pembeli membayar ke Instashop. </p>
            <p>3. Jika pembayaran sudah terverifikasi, maka status pesanan akan berubah menjadi “Menunggu Pengiriman” / “Perlu Dikirim”. <strong>TIDAK ADA BATASAN WAKTU MAKSIMAL PENGIRIMAN BARANG</strong>.</p>
            <p>4. Setelah resi dimasukkan oleh penjual, status pesanan akan menjadi “Sudah Dikirim”. Pembeli juga akan menerima SMS berisi pemberitahuan pesanan telah dikirim beserta nomor resi.</p>
            <p>5. Jika produk telah diterima dengan baik, pembeli dapat mengklik tombol <strong>“Sudah Diterima”</strong> yang ada di link web pesanannya. </p>
            <p>6. Jika setelah <strong>7 (tujuh) hari sejak resi pengiriman dimasukkan</strong> pesanan belum diterima, pembelimu dapat klik <strong>“Perpanjang Penerimaan”</strong>. Instashop akan otomatis memperpanjang masa garansi dana hingga <strong>hari ke-10 (sepuluh)</strong>. Jika di hari ketujuh pembeli tidak klik “Perpanjang Penerimaan” atau “Pengembalian Dana”, <strong>dana otomatis dicairkan ke saldo penjual</strong>.</p>
            <p>7. Jika hingga hari ke-10 pesanan belum juga diterima, pembeli dapat klik <strong>“Pengembalian Dana”</strong>. Setelah pesanan masuk ke dalam status “Pengembalian Dana”, maka dana akan kembali disimpan oleh Instashop. Dana akan dicairkan ke penjual atau dikembalikan ke pembeli setelah mendapat persetujuan dari kedua belah pihak. </p>
            <p>8. Jika pembeli tidak klik “Pengembalian Dana”, <strong>dana akan otomatis dicairkan ke penjual di hari kesebelas</strong>. Penjual dapat melihat dan menarik saldo dana tersebut di submenu “Saldo Belum Ditarik” dalam menu “Penghasilan”</p>
            <p><strong>Bagaimana Alur Pengembalian Dana?</strong></p>
            <img src="">
            <p>1. Pembeli dapat mengajukan pengembalian dana setelah pembayaran terkonfirmasi.</p>
            <p>2. Penjual dapat memilih untuk menerima atau menolak pengajuan pengembalian dana.</p>
            <p>3. Jika pengajuan pengembalian dana ditolak, maka tim CS Instashop akan menghubungi pembeli dan penjual untuk melakukan mediasi. </p>
            <p>Setelah mediasi mencapai kesepakatan, maka tim CS kami akan memproses pengembalian dana / pencairan dana sesuai hasil mediasi.</p>
            <p>4. Pencairan dana diajukan ke bank. Dana akan sampai ke rekening dalam 2 (dua) hari kerja.</p>
            <p>5. Transaksi Selesai.</p>
